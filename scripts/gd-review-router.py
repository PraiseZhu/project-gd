def main() -> int:
    # Q3: refuse if the invocation ID env is already set (side-door prevention)
    if os.environ.get(INVOCATION_ID_ENV):
        print(
            f"ERROR: SIDE_DOOR_DETECTED: {INVOCATION_ID_ENV}={os.environ[INVOCATION_ID_ENV]!r} "
            f"is set on entry — the router must not be called from within another router run. "
            f"Unset {INVOCATION_ID_ENV} before invoking directly.",
            file=sys.stderr,
        )
        return 2

    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--mode", choices=sorted(VALID_MODES), help="Router execution mode (required unless --self-test)")
    p.add_argument("--fixture", help="Path to fixture JSON (required for --mode fixture)")
    p.add_argument("--target", help="Path to review target (plan file or directory)")
    p.add_argument("--output-dir", default=None, help="Directory to write route_report.json (default: GD_ROOT/reports/review-router/)")
    p.add_argument("--invocation-id", default=None, help="Optional router invocation ID (auto-generated if absent)")
    p.add_argument("--self-test", action="store_true", help="Run self-test and exit")
    p.add_argument("--claude-review", default=None, metavar="CLAUDE_JSON",
                   help="(live plan mode) Path to Claude review mapped JSON.")
    p.add_argument("--codex-raw-result", default=None, metavar="RAW_MD",
                   help="(live plan mode) Path to existing Codex raw .result file (read-only).")
    p.add_argument("--codex-mapped-result", default=None, metavar="MAPPED_JSON",
                   help="(live plan mode) Path to pre-mapped Codex result JSON.")
    p.add_argument("--review-contract", default="auto", choices=["auto", "v1", "v2"],
                   help="Raw result parse contract for --codex-raw-result (default: auto).")
    p.add_argument("--expected-target-hash", default=None, metavar="HASH",
                   help="SHA-256 of plan at review submit time for binding check.")
    args = p.parse_args()

    if args.self_test:
        return run_self_test()

    if not args.mode:
        p.print_help()
        return 2

    invocation_id = args.invocation_id or generate_invocation_id()
    output_dir = Path(args.output_dir) if args.output_dir else GD_ROOT / "reports" / "review-router"
    target = Path(args.target) if args.target else None

    # Set invocation ID for downstream scripts (Q3: they can verify they're called from router)
    os.environ[INVOCATION_ID_ENV] = invocation_id

    if args.mode == "fixture":
        if not args.fixture:
            return err("FIXTURE_REQUIRED", "--fixture <path> is required for --mode fixture", 2)
        return run_fixture(Path(args.fixture), output_dir, invocation_id)
    elif args.mode == "detect_only":
        return run_detect_only(target, output_dir, invocation_id)
    elif args.mode == "live_dry_run":
        return run_live_dry_run(target, output_dir, invocation_id)
    elif args.mode == "live":
        return run_live(
            target, output_dir, invocation_id,
            claude_review=getattr(args, "claude_review", None),
            codex_raw_result=getattr(args, "codex_raw_result", None),
            codex_mapped_result=getattr(args, "codex_mapped_result", None),
            review_contract=getattr(args, "review_contract", "auto"),
            expected_target_hash=getattr(args, "expected_target_hash", None),
        )

    return err("UNKNOWN_MODE", args.mode, 2)


if __name__ == "__main__":
    sys.exit(main())
