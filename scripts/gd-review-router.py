def _run_live_code_only(
    target: Path,
    output_dir: Path,
    invocation_id: str,
) -> int:
    """code_only: LOCAL_STATIC_ONLY — delegated to skill_orchestrated Claude (3a-SC-5/SC-9).
    Must not claim active/live Codex sidecar.
    Stage 1: upstream quality gate (/code-review + /simplify); fail-closed if either unavailable/non-zero
    Stage 2: skill_orchestrated Claude static review (LOCAL_STATIC_ONLY)
    handler = skill_orchestrated Claude static review
    allowed decisions = APPROVED | REQUIRES_CHANGES | FAILED (failure_code=LOCAL_STATIC_ONLY)
    Always exits 1 (REQUIRES_CHANGES) since actual review done by skill_orchestrated Claude."""
    import hashlib
    output_dir.mkdir(parents=True, exist_ok=True)
    # Compute diff for audit trail
    try:
        cwd = str(target.parent if target.is_file() else target)
        r_diff = subprocess.run(
            ["git", "diff", "HEAD", "--", str(target)],
            capture_output=True, text=True, cwd=cwd,
        )
        diff_content = r_diff.stdout if r_diff.stdout else f"# no diff output; git exit={r_diff.returncode}; target={target}"
    except Exception as e:
        diff_content = f"# git diff unavailable: {e}"
    diff_hash = hashlib.sha256(diff_content.encode()).hexdigest()
    diff_path = output_dir / f"code_diff_{invocation_id[-8:]}.patch"
    diff_path.write_text(diff_content, encoding="utf-8")

    # --- Stage 1: upstream quality gate (/code-review + /simplify) after diff landing ---
    # fail-closed: any unavailability or non-zero exit short-circuits here (SC-1 / master SC-5)
    gate_result = _run_upstream_quality_gate(target, output_dir, invocation_id)
    if gate_result["fail_closed"]:
        report = {
            "schema_version": "2.0",
            "router_invocation_id": invocation_id,
            "mode": "live",
            "review_target_kind": "code_only",
            "decision": "REQUIRES_CHANGES",
            "validator_signature": VALIDATOR_SIGNATURE,
            "recorded_at": now_iso(),
            "diff_source": f"git diff HEAD -- {target}",
            "diff_hash": diff_hash,
            "patch_generation_method": "git_diff",
            "raw_result_path": str(diff_path),
            "raw_result_hash": diff_hash,
            "claude_review_origin": "skill_orchestrated",
            "failure_code": gate_result["failure_code"],
            "upstream_quality_gate": {"steps": gate_result["steps"], "fail_closed": True},
            "findings": [{
                "reviewer": "quality_gate",
                "severity": "error",
                "description": f"上游质量门失败: {gate_result['failure_code']}",
            }],
        }
        write_and_validate_route_report(report, output_dir)
        return 1  # fail-closed: upstream quality gate failed

    # Quality gate passed; record results and proceed to Codex conformance cross-review (SC-2)
    # codex_review_scope="conformance": Codex verifies implementation meets plan SC, not a full bug hunt
    upstream_quality_gate_record = {"steps": gate_result["steps"], "fail_closed": False}

    report = {
        "schema_version": "2.0",
        "router_invocation_id": invocation_id,
        "mode": "live",
        "review_target_kind": "code_only",
        "decision": "REQUIRES_CHANGES",
        "validator_signature": VALIDATOR_SIGNATURE,
        "recorded_at": now_iso(),
        "diff_source": f"git diff HEAD -- {target}",
        "diff_hash": diff_hash,
        "patch_generation_method": "git_diff",
        "raw_result_path": str(diff_path),
        "raw_result_hash": diff_hash,
        "claude_review_origin": "skill_orchestrated",
        # conformance scoping: Codex reviews whether implementation meets plan SC (SC-2)
        "codex_review_scope": "conformance",
        "failure_code": "LOCAL_STATIC_ONLY",
        # upstream quality gate results are separately observable (SC-2 separation requirement)
        "upstream_quality_gate": upstream_quality_gate_record,
        "findings": [{
            "reviewer": "router",
            "severity": "info",
            "description": (
                "code_only: static review delegated to skill_orchestrated Claude. "
                "Router records diff for audit trail; Codex code sidecar NOT active (3a-SC-9). "
                "REQUIRES_CHANGES until skill_orchestrated review completes."
            ),
        }],
    }
    # L3 content-evidence validation note (SC-W2-2):
    # code_only router does not have a real Codex review to validate at this stage.
    # The diff_path is the diff itself, not a review output. L3 would be a no-op
    # (target == review). The actual L3 gate runs in parse-transport when Codex
    # returns a real review result. No L3 call here prevents the no-op false pass.

    write_and_validate_route_report(report, output_dir)
    return 1  # REQUIRES_CHANGES — actual verdict from skill_orchestrated review


def _run_live_execution_plus_code(
    target: Path,
    output_dir: Path,
    invocation_id: str,
    codex_raw_result: Path | None = None,
    codex_mapped_result: Path | None = None,
    review_contract: str = "auto",
    live_bridge_timeout_sec: int = 360,
    use_controller: bool = False,
    claude_review_json: Path | None = None,
    deep: bool = False,
    plan_file: str | None = None,
) -> int:
    """execution_plus_code (combined): outcome-first, then code if passes (3a-SC-5/SC-7).
    Stage 1: gd-validate-execution-outcome.py
    Stage 2a (T7 controller path): multi-round convergence loop via gd-review-controller.py
             --branch combined (use_controller=True).
    Stage 2b (single-round path): LOCAL_STATIC_ONLY code review (skill_orchestrated) — backward compat.
    """
    import hashlib
    # If target is a directory, resolve to the first valid execution JSON within it
    # (execution_plus_code detection requires a dir with both .md + exec JSON, but
    # outcome validator and _get_plan_ref need a concrete file).
    if target.is_dir():
        for candidate in sorted(target.rglob("*.json")):
            if candidate.name.startswith("_"):
                continue
            if is_execution_json(candidate):
                target = candidate
                break
    plan_ref = _get_plan_ref(target)  # SC-2: read plan_ref for Phase 2 verify-rerun
    outcome_script = SCRIPTS / "gd-validate-execution-outcome.py"  # hoisted; used in both branches

    # T7: controller combined-branch path
    if use_controller:
        r_outcome = subprocess.run(
            _build_outcome_validator_cmd(outcome_script, target, plan_ref),
            capture_output=True, text=True,
            env={**os.environ, INVOCATION_ID_ENV: invocation_id},
        )
        if r_outcome.returncode != 0:
            print(
                "ERROR: OUTCOME_FIRST_FAIL: outcome validator failed before combined controller",
                file=sys.stderr,
            )
            return 1
        return _run_controller_multi_round(
            branch="combined",
            target=target,
            output_dir=output_dir,
            invocation_id=invocation_id,
            execution_result=target,
            claude_review_json=claude_review_json,
            deep=deep,
            plan_file=plan_file,
        )

    r_outcome = subprocess.run(
        _build_outcome_validator_cmd(outcome_script, target, plan_ref),
        capture_output=True, text=True,
        env={**os.environ, INVOCATION_ID_ENV: invocation_id},
    )

    if r_outcome.returncode != 0:
        # Outcome failed → OUTCOME_FIRST_FAIL; code review MUST NOT run (3a-SC-7)
        report = {
            "schema_version": "2.0",
            "router_invocation_id": invocation_id,
            "mode": "live",
            "review_target_kind": "execution_plus_code",
            "decision": "FAILED",
            "validator_signature": VALIDATOR_SIGNATURE,
            "recorded_at": now_iso(),
            "execution_artifact_ref": str(target),
            "outcome_validator_status": "not_run_blocked",
            "stage_order": ["outcome", "code"],
            "failure_code": "OUTCOME_FIRST_FAIL",
            "claude_review_origin": "not_applicable",
            "findings": [{
                "reviewer": "outcome_validator",
                "severity": "error",
                "description": (
                    "outcome validation failed; code review BLOCKED per outcome-first rule (3a-SC-7). "
                    + (r_outcome.stderr[:400] if r_outcome.stderr else "")
                ),
            }],
        }
        rc, _ = write_and_validate_route_report(report, output_dir)  # D2
        return 1  # outcome failed: no ledger needed (not APPROVED)

    # Outcome passed → Stage 1.5: upstream quality gate (/code-review + /simplify)
    # fail-closed: any unavailability or non-zero exit short-circuits here (SC-1 / master SC-5)
    output_dir.mkdir(parents=True, exist_ok=True)
    exec_hash = hashlib.sha256(target.read_bytes()).hexdigest()

    gate_result = _run_upstream_quality_gate(target, output_dir, invocation_id)
    if gate_result["fail_closed"]:
        report = {
            "schema_version": "2.0",
            "router_invocation_id": invocation_id,
            "mode": "live",
            "review_target_kind": "execution_plus_code",
            "decision": "REQUIRES_CHANGES",
            "validator_signature": VALIDATOR_SIGNATURE,
            "recorded_at": now_iso(),
            "execution_artifact_ref": str(target),
            "execution_artifact_hash": exec_hash,
            "outcome_validator_status": "passed",
            "codex_review_status": "not_run_blocked",
            "codex_review_kind": "combined",
            "codex_review_scope": "conformance",
            "stage_order": ["outcome", "code"],
            "failure_code": gate_result["failure_code"],
            "claude_review_origin": "not_applicable",
            "upstream_quality_gate": {"steps": gate_result["steps"], "fail_closed": True},
            "findings": [{
                "reviewer": "quality_gate",
                "severity": "error",
                "description": f"上游质量门失败: {gate_result['failure_code']}",
            }],
        }
        rc, _ = write_and_validate_route_report(report, output_dir)
        return 1  # fail-closed: upstream quality gate failed

    # Quality gate passed; record results and proceed to Codex conformance cross-review (SC-2)
    upstream_quality_gate_record = {"steps": gate_result["steps"], "fail_closed": False}

    # Stage 2: combined Codex conformance cross-review (Execution-Review Cross-Review v2)
    # Codex task is scoped to conformance: verify implementation meets plan SC; not a full bug hunt.
    bridge_script = SCRIPTS / "gd-codex-bridge-review.py"
    codex_status = "not_run_blocked"
    codex_decision = None
    codex_raw_path = None
    codex_raw_hash = None
    codex_mapped_path = None
    codex_mapped_hash = None
    findings: list = []

    if codex_raw_result is not None:
        mapped_out = output_dir / f"codex_combined_mapped_{invocation_id[-8:]}.json"
        parse_args = [
            sys.executable, str(bridge_script), "parse-transport",
            "--kind", "combined",
            "--target", str(target),
            "--raw-result", str(codex_raw_result),
            "--out", str(mapped_out),
        ]
        # revision=20: same compat-v1 policy as execution_only path
        if review_contract != "v2":
            parse_args.append("--compat-v1")
        if deep:
            parse_args.append("--deep")
        r_bridge = subprocess.run(parse_args, capture_output=True, text=True)
        if not mapped_out.exists():
            codex_status = "wrapper_schema_fail"
            findings.append({"reviewer": "codex_bridge", "severity": "error",
                             "description": f"bridge parse-transport failed: {r_bridge.stderr[:400]}"})
        else:
            codex_raw_path = str(codex_raw_result)
            codex_raw_hash = hashlib.sha256(codex_raw_result.read_bytes()).hexdigest()
            codex_mapped_path = str(mapped_out)
            codex_mapped_hash = hashlib.sha256(mapped_out.read_bytes()).hexdigest()
            try:
                mapped = json.loads(mapped_out.read_text(encoding="utf-8"))
                codex_decision = mapped.get("gd_review_decision") or mapped.get("decision")
                # R1: derive codex_review_status from verdict + run_status via the shared
                # SSOT helper — a FAILED/degraded mapped must NOT silently become 'completed'.
                codex_status = codex_review_status_from_evidence(
                    True,
                    str(codex_decision).strip().upper() if codex_decision else "FAILED",
                    mapped.get("review_run_status"))
                for f in mapped.get("findings", []) or []:
                    findings.append({**f, "reviewer": "codex"})
            except Exception as e:
                codex_status = "wrapper_schema_fail"
                findings.append({"reviewer": "codex_bridge", "severity": "error",
                                 "description": f"mapped JSON unreadable: {e}"})
    elif codex_mapped_result is not None:
        try:
            mapped = json.loads(codex_mapped_result.read_text(encoding="utf-8"))
            codex_decision = mapped.get("gd_review_decision") or mapped.get("decision")
            # R1: shared SSOT helper (verdict + run_status aware) instead of 2-state inline
            # that mapped FAILED/None/unknown → completed.
            codex_status = codex_review_status_from_evidence(
                True,
                str(codex_decision).strip().upper() if codex_decision else "FAILED",
                mapped.get("review_run_status"))
            codex_mapped_path = str(codex_mapped_result)
            codex_mapped_hash = hashlib.sha256(codex_mapped_result.read_bytes()).hexdigest()
            codex_raw_path = str(codex_mapped_result) + ".raw_unknown"
            codex_raw_hash = "0" * 64
            for f in mapped.get("findings", []) or []:
                findings.append({**f, "reviewer": "codex"})
        except Exception as e:
            codex_status = "wrapper_schema_fail"
            findings.append({"reviewer": "codex_bridge", "severity": "error",
                             "description": f"mapped JSON unreadable: {e}"})
    else:
        # C3: no injection → call live bridge with kind=combined (was transport_failed).
        bridge_result = _run_live_codex_bridge(
            kind="combined",
            target=target,
            output_dir=output_dir,
            invocation_id=invocation_id,
            timeout_sec=live_bridge_timeout_sec,
            deep=deep,
            plan_file=plan_file,
        )
        codex_status = bridge_result["status"]
        codex_decision = bridge_result["decision"]
        codex_raw_path = bridge_result["raw_path"]
        codex_raw_hash = bridge_result["raw_hash"]
        codex_mapped_path = bridge_result["mapped_path"]
        codex_mapped_hash = bridge_result["mapped_hash"]
        findings.extend(bridge_result["findings"])

    final_decision = "APPROVED" if (codex_status == "completed" and codex_decision in (None, "APPROVED")) else "REQUIRES_CHANGES"

    report = {
        "schema_version": "2.0",
        "router_invocation_id": invocation_id,
        "mode": "live",
        "review_target_kind": "execution_plus_code",
        "decision": final_decision,
        "validator_signature": VALIDATOR_SIGNATURE,
        "recorded_at": now_iso(),
        "execution_artifact_ref": str(target),
        "execution_artifact_hash": exec_hash,
        "outcome_validator_status": "passed",
        "stage_order": ["outcome", "code"],
        "codex_review_kind": "combined",
        "codex_review_status": codex_status,
        # conformance scoping: Codex reviews whether implementation meets plan SC (SC-2)
        "codex_review_scope": "conformance",
        "claude_review_origin": "not_applicable",
        # upstream quality gate results are separately observable (SC-2 separation requirement)
        "upstream_quality_gate": upstream_quality_gate_record,
        "findings": findings,
    }
    if codex_raw_path is not None:
        report["codex_raw_result_path"] = codex_raw_path
        report["codex_raw_result_hash"] = codex_raw_hash
    if codex_mapped_path is not None:
        report["codex_mapped_result_path"] = codex_mapped_path
        report["codex_mapped_result_hash"] = codex_mapped_hash
    if codex_status == "transport_failed":
        report["failure_code"] = "CODEX_TRANSPORT_UNAVAILABLE"
    elif codex_status == "wrapper_schema_fail":
        report["failure_code"] = "CODEX_WRAPPER_SCHEMA_FAIL"

    # D1: generate child review ledger (outcome_validator + codex_bridge as 2 child jobs)
    ledger_path = output_dir / f"child_review_ledger_combined_{invocation_id[-8:]}.json"
    report["child_review_ledger_path"] = str(ledger_path)

    # D2 + D1: the child ledger must exist before route-report validation. The
    # write -> ledger -> validate ordering is centralized in
    # write_and_validate_route_report (via ledger_writer) so this path — and any
    # future ledger-bearing path — cannot get the ordering wrong.
    def _write_ledger(rp):
        _write_execution_review_ledger(
            ledger_path=ledger_path,
            invocation_id=invocation_id,
            stage="review_execution_code",
            codex_review_kind="combined",
            outcome_validator_status="passed",
            exec_hash=exec_hash,
            target=target,
            codex_status=codex_status,
            codex_decision=codex_decision,
            codex_mapped_path=codex_mapped_path,
            codex_mapped_hash=codex_mapped_hash,
            codex_raw_path=codex_raw_path,
            codex_raw_hash=codex_raw_hash,
            route_report_path=rp,
            final_decision=final_decision,
        )

    rc, _ = write_and_validate_route_report(report, output_dir, ledger_writer=_write_ledger)
    if rc != 0:  # propagate validator rc
        return 1
    return 0 if final_decision == "APPROVED" else 1


def run_live(
    target: Path | None,
    output_dir: Path,
    invocation_id: str,
    claude_review: str | None = None,
    codex_raw_result: str | None = None,
    codex_mapped_result: str | None = None,
    review_contract: str = 'auto',
    expected_target_hash: str | None = None,
    live_bridge_timeout_sec: int = 360,
    deep: bool = False,
    plan_file: str | None = None,
) -> int:
    print(f"=== gd-review-router: live mode ===")
    print(f"target    : {target or '(auto)'}")
    print()

    kind = detect_review_target_kind(target)
    print(f"detected target kind: {kind}")

    # P2 fail-closed: a self-declared-but-corrupt router descriptor is reported
    # with a DISTINCT failure_code from a genuine no-artifact target, so the
    # operator can tell "you pointed at a broken descriptor" apart from "there
    # was nothing to review". Both still fail-closed (exit 2, never APPROVE).
    if kind == CORRUPT_DESCRIPTOR_SENTINEL:
        report = {
            "schema_version": "2.0",
            "router_invocation_id": invocation_id,
            "mode": "live",
            "review_target_kind": "no_artifact",
            "decision": "REVIEW_TARGET_MISSING",
            "validator_signature": VALIDATOR_SIGNATURE,
            "recorded_at": now_iso(),
            "failure_code": "CORRUPT_ROUTER_DESCRIPTOR",
            "claude_review_origin": "not_applicable",
            "findings": [{"reviewer": "router", "severity": "error",
                          "description": "CORRUPT_ROUTER_DESCRIPTOR: target declared a v1 router "
                                         "descriptor (router_version + artifacts) but it is "
                                         "unreadable/malformed; fail-closed, distinct from "
                                         "NO_ARTIFACT_DETECTED. Must not APPROVE."}],
        }
        write_and_validate_route_report(report, output_dir)
        print("ERROR: CORRUPT_ROUTER_DESCRIPTOR: descriptor present but malformed; fail-closed",
              file=sys.stderr)
        return 2

    if kind == "no_artifact":
        report = {
            "schema_version": "2.0",
            "router_invocation_id": invocation_id,
            "mode": "live",
            "review_target_kind": "no_artifact",
            "decision": "REVIEW_TARGET_MISSING",
            "validator_signature": VALIDATOR_SIGNATURE,
            "recorded_at": now_iso(),
            "failure_code": "NO_ARTIFACT_DETECTED",
            "claude_review_origin": "not_applicable",
            "findings": [{"reviewer": "router", "severity": "error",
                          "description": "REVIEW_TARGET_MISSING: no reviewable artifact detected; "
                                         "fail-closed (3a-SC-6). Must not APPROVE."}],
        }
        write_and_validate_route_report(report, output_dir)
        print("ERROR: REVIEW_TARGET_MISSING: no reviewable artifact detected; fail-closed", file=sys.stderr)
        return 2  # exit 2 = missing input / no artifact (3a-SC-4/SC-6)

    # Plan 1: plan_only kind can be handled via subprocess merge loop with Claude + Codex inputs
    if kind == "plan_only" and target is not None:
        return _run_live_plan_review(
            target=target,
            output_dir=output_dir,
            invocation_id=invocation_id,
            claude_review=claude_review,
            codex_raw_result=codex_raw_result,
            codex_mapped_result=codex_mapped_result,
            review_contract=review_contract,
            expected_target_hash=expected_target_hash,
        )

    # Plan 3a: non-plan target kinds — all have defined live behavior (3a-SC-5).
    # No NOT_IMPLEMENTED. Handlers below.
    # Injected raw/mapped (offline/fixture) → single-round consume (Path A/B);
    # only drive the T7 multi-round controller when nothing was injected (true live).
    # Named once so the two execution handlers below cannot drift.
    is_true_live = codex_raw_result is None and codex_mapped_result is None
    if kind == "execution_only_no_code" and target is not None:
        return _run_live_execution_only(
            target, output_dir, invocation_id,
            codex_raw_result=(Path(codex_raw_result) if codex_raw_result else None),
            codex_mapped_result=(Path(codex_mapped_result) if codex_mapped_result else None),
            review_contract=review_contract,
            live_bridge_timeout_sec=live_bridge_timeout_sec,
            use_controller=is_true_live,
            deep=deep,
            plan_file=plan_file,
        )

    if kind == "code_only" and target is not None:
        return _run_live_code_only(target, output_dir, invocation_id)

    if kind == "execution_plus_code" and target is not None:
        return _run_live_execution_plus_code(
            target, output_dir, invocation_id,
            codex_raw_result=(Path(codex_raw_result) if codex_raw_result else None),
            codex_mapped_result=(Path(codex_mapped_result) if codex_mapped_result else None),
            review_contract=review_contract,
            live_bridge_timeout_sec=live_bridge_timeout_sec,
            use_controller=is_true_live,
            deep=deep,
            plan_file=plan_file,
        )

    # Fallback: target is None for a non-plan kind — fail-closed.
    report = {
        "schema_version": "2.0",
        "router_invocation_id": invocation_id,
        "mode": "live",
        "review_target_kind": kind,
        "decision": "FAILED",
        "validator_signature": VALIDATOR_SIGNATURE,
        "recorded_at": now_iso(),
        "failure_code": "TARGET_REQUIRED_FOR_KIND",
        "claude_review_origin": "not_applicable",
        "findings": [{"reviewer": "router", "severity": "error",
                      "description": f"live mode: --target required for kind={kind}"}],
    }
    if kind in ("execution_only_no_code", "execution_plus_code"):
        report["execution_artifact_ref"] = "(missing)"
        report["outcome_validator_status"] = "not_run_blocked"
    if kind == "execution_plus_code":
        report["stage_order"] = ["outcome", "code"]
    write_and_validate_route_report(report, output_dir)
    return 1


