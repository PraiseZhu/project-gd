def run_self_test() -> int:
    errors: list[str] = []

    print("=== gd-review-router self-test ===")

    # 1. Route report validator exists
    if not ROUTE_REPORT_VALIDATOR.exists():
        errors.append(f"ROUTE_REPORT_VALIDATOR missing: {ROUTE_REPORT_VALIDATOR}")
    else:
        print(f"  route report validator present ✓")

    # 2. fixtures/review-router-v2/ exists and has content
    if not FIXTURES_V2.exists():
        errors.append(f"FIXTURES_V2 dir missing: {FIXTURES_V2}")
    else:
        v2_files = list(FIXTURES_V2.glob("*.json"))
        if not v2_files:
            errors.append(f"FIXTURES_V2 dir empty: {FIXTURES_V2}")
        else:
            print(f"  v2 fixtures: {len(v2_files)} files ✓")

    # 3. Q3 env check: INVOCATION_ID_ENV must be absent on entry
    if os.environ.get(INVOCATION_ID_ENV):
        errors.append(
            f"SIDE_DOOR_DETECTED: {INVOCATION_ID_ENV} is set; "
            "self-test should not be called from within a router run"
        )
    else:
        print(f"  Q3 side-door env absent ✓")

    # 4. Smoke: fixture mode with a known-good fixture
    good_fx = FIXTURES_V2 / "plan-only-approved.json"
    if good_fx.exists():
        import tempfile
        with tempfile.TemporaryDirectory() as td:
            iid = generate_invocation_id()
            rc = run_fixture(good_fx, Path(td), iid)
        if rc == 0:
            print(f"  fixture smoke (plan-only-approved): PASS ✓")
        else:
            errors.append("fixture smoke (plan-only-approved) FAIL")
    else:
        errors.append(f"smoke fixture not found: {good_fx}")

    if errors:
        print("\nSELF_TEST FAIL:", file=sys.stderr)
        for e in errors:
            print(f"  - {e}", file=sys.stderr)
        return 1
    print("\nSELF_TEST: PASS")
    return 0


