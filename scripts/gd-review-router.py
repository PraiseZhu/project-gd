#!/usr/bin/env python3
"""
gd-review-router.py — Plan 5 v3 Wave B

Unified /gd review state machine router.

Modes (--mode, required):
  fixture       Replay a pre-recorded fixture from fixtures/review-router-v2/.
  detect_only   Real target detection; no review invoked; writes route report.
  live_dry_run  Real detection + mock bridge wrapper; writes route report with mock flag.
  live          Full production run (requires Codex bridge available).

Route report:
  After every run the router writes a route_report.json and immediately validates
  it via scripts/gd-validate-route-report.py (SSOT). Consumers must verify
  validator_signature before trusting the report (Q2).

Q3 side-door prevention:
  The env var GD_REVIEW_ROUTER_INVOCATION_ID must NOT be set when invoking the router
  directly (it is only set BY the router for downstream scripts it spawns). A non-empty
  env value on entry signals an attempted side-door bypass and causes exit 2.

CLI:
  python3 scripts/gd-review-router.py --mode fixture --fixture <path.json> [--output-dir <dir>]
  python3 scripts/gd-review-router.py --mode detect_only [--target <path>] [--output-dir <dir>]
  python3 scripts/gd-review-router.py --mode live_dry_run [--target <path>] [--output-dir <dir>]
  python3 scripts/gd-review-router.py --mode live [--target <path>] [--output-dir <dir>]
  python3 scripts/gd-review-router.py --self-test

退出码（Plan 3a canonical — 3a-SC-4）：
  0 = APPROVED / route success
  1 = REQUIRES_CHANGES / routing decision non-APPROVED / validation failed
  2 = fail-closed missing input / usage error / side-door detection / no reviewable artifact

claude_review_origin field (Plan 3a — 3a-SC-2):
  skill_orchestrated  — Claude review generated by /gd command skill orchestration;
                         router is a consumer only; router does NOT spawn Claude subprocess.
  supplied            — Claude review JSON passed via --claude-review argument (direct CLI).
  not_applicable      — target kind does not use Claude review (execution_only, no_artifact).
"""
