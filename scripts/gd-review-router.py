def _is_router_descriptor(path: Path) -> bool:
    """Return True if a JSON file is a v1 router input descriptor (has router_version + artifacts)."""
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
        if isinstance(data, dict):
            return "router_version" in data and "artifacts" in data
    except Exception:
        pass
    return False


# P2 sentinel: a JSON target that declared itself a router descriptor
# (router_version + artifacts keys present) but is unreadable/malformed at
# classification time. This is DISTINCT from a genuinely empty target
# ("no_artifact"): a corrupt descriptor means the caller intended to point at
# artifacts but the descriptor itself is broken — we surface a different
# failure_code so it is not silently bucketed as "nothing to review".
CORRUPT_DESCRIPTOR_SENTINEL = "corrupt_descriptor"


def _classify_router_descriptor(path: Path) -> str:
    """Classify a v1 router-input descriptor via the shared SSOT helper.

    Mirrors gd-detect-review-target.detect_kind() so router/detector parity is
    preserved without a subprocess hop. Returns CORRUPT_DESCRIPTOR_SENTINEL when
    a self-declared descriptor cannot be parsed/validated (P2 fail-closed),
    which the caller maps to a CORRUPT_ROUTER_DESCRIPTOR failure_code rather than
    NO_ARTIFACT_DETECTED.
    """
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return CORRUPT_DESCRIPTOR_SENTINEL
    artifacts = (data or {}).get("artifacts", {}) if isinstance(data, dict) else {}
    if not isinstance(artifacts, dict):
        return CORRUPT_DESCRIPTOR_SENTINEL
    return classify_artifacts(
        bool(artifacts.get("plan_files")),
        bool(artifacts.get("execution_files")),
        bool(artifacts.get("code_diff")),
        bool(artifacts.get("bundle_declared", False)),
    )


def detect_review_target_kind(target: Path | None) -> str:
    """Target detection for detect_only / live_dry_run / live modes.

    Returns one of the REVIEW_TARGET_KIND_ENUM values. Plan 8 v4.1 Step 5
    delegates the artifact decision matrix to gd_review_detection.classify_artifacts
    so this function only handles file-system → (has_plan, has_exec, has_code)
    mapping. ``bundle_declared`` is treated as False for filesystem targets;
    explicit bundles must come through router-input JSON descriptors.
    """
    if target is None or not target.exists():
        return "no_artifact"

    if target.is_file():
        if target.suffix == ".md":
            return classify_artifacts(True, False, False)
        if target.suffix == ".json":
            # v1 router descriptor → use its declared artifacts.
            if _is_router_descriptor(target):
                return _classify_router_descriptor(target)
            # Raw execution artifact → content-based inspection.
            if is_execution_json(target):
                return classify_artifacts(False, True, False)
        return "no_artifact"

    if target.is_dir():
        has_plan = any(target.glob("**/*.md"))
        has_exec = has_execution_artifacts_in_dir(target)
        # Filesystem walks have no bundle marker → keep bundle_declared False.
        return classify_artifacts(has_plan, has_exec, False)

    return "no_artifact"


