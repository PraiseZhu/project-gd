def run_review_subcommand(sub: str, rest: list[str]) -> int:
    """SC-9/SC-17/SC-25: `review execution|code [--deep --plan-file --out --target]` subcommand.

    Thin wrapper over _run_live_codex_bridge that forces the review kind:
      review execution → execution_outcome
      review code      → code_diff
    Sets the G1 sentinel invocation id, runs the live deep bridge, and writes the
    mapped result JSON to --out (single file). Fails closed (non-zero) if the bridge
    does not produce a mapped result — never writes a pass result on transport failure.
    """
    kind = "execution_outcome" if sub == "execution" else "code_diff"
    sp = argparse.ArgumentParser(prog=f"gd-review-router.py review {sub}")
    sp.add_argument("--target", required=True, help="Review target (outcome JSON or code path)")
    sp.add_argument("--out", required=True, help="Single-file path to write mapped result JSON")
    sp.add_argument("--deep", action="store_true", default=False,
                    help="Deep review mode (workspace-write sandbox; bridge timeout ≥1800s)")
    sp.add_argument("--plan-file", default=None, help="Plan file for deep outcome capsule (plan-derived verify)")
    sp.add_argument("--output-dir", default=None, help="Working dir for intermediate bridge artifacts")
    sp.add_argument("--live-bridge-timeout-sec", type=int, default=360)
    a = sp.parse_args(rest)

    if os.environ.get(INVOCATION_ID_ENV):
        return err("SIDE_DOOR_DETECTED",
                   f"{INVOCATION_ID_ENV} already set; unset before invoking router subcommand", 2)

    invocation_id = generate_invocation_id()
    out_path = Path(a.out)
    work_dir = Path(a.output_dir) if a.output_dir else out_path.parent / f".review-{sub}-{invocation_id[-8:]}"
    work_dir.mkdir(parents=True, exist_ok=True)
    os.environ[INVOCATION_ID_ENV] = invocation_id
    try:
        res = _run_live_codex_bridge(
            kind=kind, target=Path(a.target), output_dir=work_dir,
            invocation_id=invocation_id,
            timeout_sec=a.live_bridge_timeout_sec,
            deep=a.deep, plan_file=a.plan_file,
        )
    finally:
        os.environ.pop(INVOCATION_ID_ENV, None)

    # Fail closed: no mapped result → do not write a pass outcome
    mapped_path = res.get("mapped_path")
    if not mapped_path or not Path(mapped_path).exists():
        print(f"REVIEW_SUBCOMMAND_FAILED: {sub} — {res.get('failure_description') or res.get('status')}",
              file=sys.stderr)
        return 1
    try:
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text(Path(mapped_path).read_text(encoding="utf-8"), encoding="utf-8")
    except OSError as exc:
        print(f"REVIEW_SUBCOMMAND_WRITE_ERROR: {exc}", file=sys.stderr)
        return 1
    decision = res.get("decision")
    print(f"REVIEW_SUBCOMMAND: {sub}")
    print(f"GD_REVIEW_DECISION: {decision}")
    print(f"MAPPED_RESULT: {out_path}")
    return 0 if decision == "APPROVED" else 1


def main() -> int:
    # SC-9: `review execution|code ...` subcommand form (translated to live deep bridge).
    # Must run BEFORE the side-door check because the subcommand sets the invocation id itself.
    argv = sys.argv[1:]
    if len(argv) >= 2 and argv[0] == "review" and argv[1] in ("execution", "code"):
        return run_review_subcommand(argv[1], argv[2:])

    # Q3: refuse if the invocation ID env is already set (side-door prevention)
    if os.environ.get(INVOCATION_ID_ENV):
        print(
            f"ERROR: SIDE_DOOR_DETECTED: {INVOCATION_ID_ENV}={os.environ[INVOCATION_ID_ENV]!r} "
            f"is set on entry — the router must not be called from within another router run. "
            f"Unset {INVOCATION_ID_ENV} before invoking directly.",
            file=sys.stderr,
        )
        return 2

    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--mode", choices=sorted(VALID_MODES), help="Router execution mode (required unless --self-test)")
    p.add_argument("--fixture", help="Path to fixture JSON (required for --mode fixture)")
    p.add_argument("--target", help="Path to review target (plan file or directory)")
    p.add_argument("--output-dir", default=None, help="Directory to write route_report.json (default: GD_ROOT/reports/review-router/)")
    p.add_argument("--invocation-id", default=None, help="Optional router invocation ID (auto-generated if absent)")
    p.add_argument("--self-test", action="store_true", help="Run self-test and exit")
    p.add_argument("--claude-review", default=None, metavar="CLAUDE_JSON",
                   help="(live plan mode) Path to Claude review mapped JSON.")
    p.add_argument("--codex-raw-result", default=None, metavar="RAW_MD",
                   help="(live plan mode) Path to existing Codex raw .result file (read-only).")
    p.add_argument("--codex-mapped-result", default=None, metavar="MAPPED_JSON",
                   help="(live plan mode) Path to pre-mapped Codex result JSON.")
    p.add_argument("--review-contract", default="auto", choices=["auto", "v1", "v2"],
                   help="Raw result parse contract for --codex-raw-result (default: auto).")
    p.add_argument("--expected-target-hash", default=None, metavar="HASH",
                   help="SHA-256 of plan at review submit time for binding check.")
    p.add_argument("--live-bridge-timeout-sec", type=int, default=360, metavar="SEC",
                   help="Timeout (seconds) for live Codex bridge run-bridge subprocess. "
                        "Default 360 (codex-send-wait default is 300; must be longer). "
                        "On timeout: codex_review_status=transport_failed fail-closed.")
    p.add_argument("--deep", action="store_true", default=False,
                   help="SC-9: deep review mode; bridge timeout ≥1800s")
    p.add_argument("--plan-file", default=None,
                   help="SC-33: plan file path for deep outcome capsule")
    args = p.parse_args()

    if args.self_test:
        return run_self_test()

    if not args.mode:
        p.print_help()
        return 2

    invocation_id = args.invocation_id or generate_invocation_id()
    output_dir = Path(args.output_dir) if args.output_dir else GD_ROOT / "reports" / "review-router"
    target = Path(args.target) if args.target else None

    # Set invocation ID for downstream scripts (Q3: they can verify they're called from router)
    os.environ[INVOCATION_ID_ENV] = invocation_id

    if args.mode == "fixture":
        if not args.fixture:
            return err("FIXTURE_REQUIRED", "--fixture <path> is required for --mode fixture", 2)
        return run_fixture(Path(args.fixture), output_dir, invocation_id)
    elif args.mode == "detect_only":
        return run_detect_only(target, output_dir, invocation_id)
    elif args.mode == "live_dry_run":
        return run_live_dry_run(target, output_dir, invocation_id)
    elif args.mode == "live":
        return run_live(
            target, output_dir, invocation_id,
            claude_review=getattr(args, "claude_review", None),
            codex_raw_result=getattr(args, "codex_raw_result", None),
            codex_mapped_result=getattr(args, "codex_mapped_result", None),
            review_contract=getattr(args, "review_contract", "auto"),
            expected_target_hash=getattr(args, "expected_target_hash", None),
            live_bridge_timeout_sec=getattr(args, "live_bridge_timeout_sec", 360),
            deep=getattr(args, "deep", False),
            plan_file=getattr(args, "plan_file", None),
        )

    return err("UNKNOWN_MODE", args.mode, 2)


if __name__ == "__main__":
    sys.exit(main())
